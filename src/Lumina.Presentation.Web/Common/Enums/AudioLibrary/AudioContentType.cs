namespace Lumina.Presentation.Web.Common.Enums.AudioLibrary;

/// <summary>
/// Enumeration for the types of audio content libraries.
/// </summary>
public enum AudioContentType
{
    /// <summary>
    /// A musical composition typically consisting of vocals and/or instruments.
    /// </summary>
    Music,

    /// <summary>
    /// A recording of a book or other written work being read aloud.
    /// </summary>
    AudioBook,

    /// <summary>
    /// An audio recording of a live musical or spoken word performance.
    /// </summary>
    LivePerformance,

    /// <summary>
    /// A recorded conversation where one person asks questions and another provides answers.
    /// </summary>
    Interview,

    /// <summary>
    /// A new performance or recording of a previously released song.
    /// </summary>
    Cover,

    /// <summary>
    /// An alternative version of a recorded song, typically with altered instrumentation or structure.
    /// </summary>
    Remix,

    /// <summary>
    /// An audio recording used to create a specific acoustic environment or impression.
    /// </summary>
    Soundeffect,

    /// <summary>
    /// A digital audio file made available on the internet for downloading to a computer or mobile device.
    /// </summary>
    Podcast,

    /// <summary>
    /// A regularly scheduled broadcast program consisting of music, talk, or other audio content.
    /// </summary>
    RadioShow,

    /// <summary>
    /// A collection of music used as background sound for a movie, TV show, video game, or other media.
    /// </summary>
    Soundtrack,
}