namespace Lumina.Presentation.Web.Common.Enums.FileSystem;

/// <summary>
/// Specifies the size options for file system icons.
/// </summary>
public enum FileSystemIconSize
{
    /// <summary>
    /// Represents the smallest icon size, typically used in compact list views or when space is limited.
    /// </summary>
    Small,

    /// <summary>
    /// Represents a medium-sized icon, balancing visibility and space efficiency.
    /// </summary>
    Medium,

    /// <summary>
    /// Represents a large icon size, providing enhanced visibility and detail.
    /// </summary>
    Large,

    /// <summary>
    /// Represents the largest icon size, offering maximum visibility and detail, suitable for high-resolution displays or thumbnail views.
    /// </summary>
    ExtraLarge
}