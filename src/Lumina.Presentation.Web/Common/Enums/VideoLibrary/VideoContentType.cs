namespace Lumina.Presentation.Web.Common.Enums.VideoLibrary;

/// <summary>
/// Enumeration for the types of video content libraries.
/// </summary>
public enum VideoContentType
{
    /// <summary>
    /// A series of related episodes broadcast on television or streaming platforms.
    /// </summary>
    TvShow,

    /// <summary>
    /// A feature-length film produced for cinema or home viewing.
    /// </summary>
    Movie,

    /// <summary>
    /// A collection of related films released in succession.
    /// </summary>
    FilmSeries,

    /// <summary>
    /// A collection of related films, TV shows, and other media sharing a fictional universe.
    /// </summary>
    FilmFranchise,

    /// <summary>
    /// A live musical performance recorded on video.
    /// </summary>
    Concert,

    /// <summary>
    /// A non-fictional film or TV program presenting facts and information about a specific subject.
    /// </summary>
    Documentary,

    /// <summary>
    /// An instructional video designed to teach a skill or provide information on a specific topic.
    /// </summary>
    Tutorial,

    /// <summary>
    /// Privately recorded videos, often for personal use or sharing with friends and family.
    /// </summary>
    Personal,

    /// <summary>
    /// Japanese animated productions, typically characterized by colorful graphics and vibrant characters.
    /// </summary>
    Anime,

    /// <summary>
    /// Videos hosted on or originating from the YouTube platform.
    /// </summary>
    Youtube,

    /// <summary>
    /// A short video, typically set to music, such as a music video or promotional clip.
    /// </summary>
    Videoclip,
}