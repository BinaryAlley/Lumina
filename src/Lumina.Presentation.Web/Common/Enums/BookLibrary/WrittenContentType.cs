namespace Lumina.Presentation.Web.Common.Enums.BookLibrary;

/// <summary>
/// Enumeration for the types of written content libraries.
/// </summary>
public enum WrittenContentType
{
    /// <summary>
    /// A printed or written literary work.
    /// </summary>
    Book,

    /// <summary>
    /// An electronic version of a printed book.
    /// </summary>
    EBook,

    /// <summary>
    /// A set of related books published in succession.
    /// </summary>
    BookSeries,

    /// <summary>
    /// A magazine or book containing sequential art in the form of a narrative.
    /// </summary>
    ComicBook,

    /// <summary>
    /// A set of related comic books published in succession.
    /// </summary>
    ComicSeries,

    /// <summary>
    /// A periodical publication containing articles, stories, and other content.
    /// </summary>
    Magazine,

    /// <summary>
    /// A style of Japanese comic books and graphic novels.
    /// </summary>
    Manga,

    /// <summary>
    /// A novel in comic-strip format, typically with sophisticated artwork and complex storylines.
    /// </summary>
    GraphicNovel,

    /// <summary>
    /// A scholarly article presenting original research or analysis.
    /// </summary>
    AcademicPaper,

    /// <summary>
    /// A handwritten or printed form of musical notation.
    /// </summary>
    SheetMusic,

    /// <summary>
    /// A script for a film, television program, or other visual media.
    /// </summary>
    Screenplay,
}