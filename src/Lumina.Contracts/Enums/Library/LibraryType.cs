namespace Lumina.Contracts.Enums.Library;

/// <summary>
/// Enumeration for the types of libraries.
/// </summary>
public enum LibraryType
{
    /// <summary>
    /// Content that is written, such as books, articles, or other written works.
    /// </summary>
    WrittenContent,

    /// <summary>
    /// Content that is video, such as movies, TV shows, or other video works.
    /// </summary>
    VideoContent,

    /// <summary>
    /// Content that is audio, such as podcasts, radio shows, or other audio works.
    /// </summary>
    AudioContent,

    /// <summary>
    /// Content that is visual, such as images, paintings, or other visual works.
    /// </summary>
    VisualContent,

    /// <summary>
    /// Content that is miscellaneous, such as lyrics, movie posters, or other miscellaneous works.
    /// </summary>
    MiscellaneousContent,
}