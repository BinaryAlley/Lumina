namespace Lumina.Domain.Common.Enums.MediaLibrary;

/// <summary>
/// Enumeration for the types of libraries.
/// </summary>
public enum LibraryType
{
    /// <summary>
    /// Traditional printed books in physical format.
    /// </summary>
    Book,

    /// <summary>
    /// Digital books in electronic format (e.g., PDF, EPUB, MOBI).
    /// </summary>
    EBook,

    /// <summary>
    /// Comic books in digital or physical format.
    /// </summary>
    ComicBook,

    /// <summary>
    /// Periodical publications containing articles and illustrations.
    /// </summary>
    Magazine,

    /// <summary>
    /// Periodical publication containing news.
    /// </summary>
    Newspaper,

    /// <summary>
    /// Japanese-style comics and graphic novels.
    /// </summary>
    Manga,

    /// <summary>
    /// Extended comic-format narratives, typically containing a complete story.
    /// </summary>
    GraphicNovel,

    /// <summary>
    /// Scholarly articles and research papers.
    /// </summary>
    AcademicPaper,

    /// <summary>
    /// Musical notation and scores.
    /// </summary>
    SheetMusic,

    /// <summary>
    /// Television series and episodic content.
    /// </summary>
    TvShow,

    /// <summary>
    /// Feature-length films and motion pictures.
    /// </summary>
    Movie,

    /// <summary>
    /// Non-fiction films presenting factual information.
    /// </summary>
    Documentary,

    /// <summary>
    /// Japanese animation series and films.
    /// </summary>
    Anime,

    /// <summary>
    /// Recorded live music performances and concerts.
    /// </summary>
    ConcertVideo,

    /// <summary>
    /// Educational and instructional videos.
    /// </summary>
    TutorialVideo,

    /// <summary>
    /// Personally recorded videos and family footage.
    /// </summary>
    HomeVideo,

    /// <summary>
    /// Videos sourced from YouTube platform.
    /// </summary>
    YouTubeVideo,

    /// <summary>
    /// Professional or amateur music video productions.
    /// </summary>
    MusicVideo,

    /// <summary>
    /// Videos recorded during live performances or events.
    /// </summary>
    LiveRecordingVideo,

    /// <summary>
    /// Recorded conversations and Q&A sessions in video format.
    /// </summary>
    InterviewVideo,

    /// <summary>
    /// Videos of musical performances covering other artists' work.
    /// </summary>
    CoverSongVideo,

    /// <summary>
    /// Video format podcasts and episodic content.
    /// </summary>
    PodcastVideo,

    /// <summary>
    /// Audio recordings of musical content.
    /// </summary>
    Music,

    /// <summary>
    /// Narrated books in audio format.
    /// </summary>
    Audiobook,

    /// <summary>
    /// Audio recordings from live performances or events.
    /// </summary>
    LiveRecordingAudio,

    /// <summary>
    /// Recorded conversations and Q&A sessions in audio format.
    /// </summary>
    InterviewAudio,

    /// <summary>
    /// Audio recordings of musical performances covering other artists' work.
    /// </summary>
    CoverSongAudio,

    /// <summary>
    /// Modified or reinterpreted versions of original musical recordings.
    /// </summary>
    Remix,

    /// <summary>
    /// Audio clips used for sound design and production.
    /// </summary>
    SoundEffect,

    /// <summary>
    /// Audio format podcasts and episodic content.
    /// </summary>
    PodcastAudio,

    /// <summary>
    /// Digital images and photographs.
    /// </summary>
    Photo,

    /// <summary>
    /// Curated sequences of media items.
    /// </summary>
    Playlist,

    /// <summary>
    /// Grouped sets of related media items.
    /// </summary>
    Collection,

    /// <summary>
    /// Text files containing dialogue translations for video content.
    /// </summary>
    Subtitles,

    /// <summary>
    /// Text files containing words and timing for musical content.
    /// </summary>
    Lyrics,
        
    /// <summary>
    /// A collection of media that does not fit into any other category.
    /// </summary>
    Other
}
