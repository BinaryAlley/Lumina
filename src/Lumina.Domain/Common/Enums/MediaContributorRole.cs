namespace Lumina.Domain.Common.Enums;

/// <summary>
/// Enumeration for the various roles of media contributors across different types of media.
/// </summary>
public enum MediaContributorRole
{
    /// <summary>
    /// A person who portrays a character in a performance, typically in films, television shows, or theater productions.
    /// </summary>
    Actor,

    /// <summary>
    /// The person responsible for overseeing and directing the production of a film, play, television show, or other performing art.
    /// </summary>
    Director,

    /// <summary>
    /// A person who uses their voice to produce musical sounds, typically with a melody and often with lyrics in songs.
    /// </summary>
    Singer,

    /// <summary>
    /// The creator or originator of a written work, such as a book, article, or other literary piece.
    /// </summary>
    Author
}