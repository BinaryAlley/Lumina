namespace Lumina.Domain.Common.Enums.PhotoLibrary;

/// <summary>
/// Enumeration for known image types.
/// </summary>
public enum ImageType
{
    /// <summary>
    /// No image type specified.
    /// </summary>
    None,

    /// <summary>
    /// Bitmap image format.
    /// </summary>
    BMP,

    /// <summary>
    /// Graphics Interchange Format (GIF).
    /// </summary>
    GIF,

    /// <summary>
    /// Portable Network Graphics (PNG) format.
    /// </summary>
    PNG,

    /// <summary>
    /// Tagged Image File Format (TIFF).
    /// </summary>
    TIFF,

    /// <summary>
    /// Joint Photographic Experts Group (JPEG) format.
    /// </summary>
    JPEG,

    /// <summary>
    /// Canon-specific JPEG format.
    /// </summary>
    JPEG_CANON,

    /// <summary>
    /// Unknown JPEG format.
    /// </summary>
    JPEG_UNKNOWN,

    /// <summary>
    /// Macintosh Picture (PICT) format.
    /// </summary>
    PICT,

    /// <summary>
    /// Icon (ICO) format.
    /// </summary>
    ICO,

    /// <summary>
    /// Photoshop Document (PSD) format.
    /// </summary>
    PSD,

    /// <summary>
    /// JPEG 2000 format.
    /// </summary>
    JPEG2000,

    /// <summary>
    /// AV1 Image File Format (AVIF).
    /// </summary>
    AVIF,

    /// <summary>
    /// Web Picture (WEBP) format.
    /// </summary>
    WEBP,

    /// <summary>
    /// Truevision TGA format.
    /// </summary>
    TGA,

    /// <summary>
    /// Scalable Vector Graphics (SVG) format.
    /// </summary>
    SVG
}
